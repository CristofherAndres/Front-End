let num1 = prompt("Ingrese un número 1")
let num2 = prompt("Ingrese un número 2")
let num3 = prompt("Ingrese un número 3")
let num4 = prompt("Ingrese un número 4")

let suma = parseInt(num1) + parseInt(num2)
let producto = parseInt(num3) * parseInt(num4)

document.write("La suma de los dos primeros números es: " + suma)
document.write("<br>")
document.write("El producto de los dos últimos números es: " + producto)